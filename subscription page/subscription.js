function changeColorSignUp(id) {
    document.getElementById(id).style.color = "blue";
    document.getElementById(id).style.backgroundColor = "hsl(150, 100%, 50%)";
}

function MouseMoveON(id) {
    document.getElementById(id).style.backgroundColor = "hsl(30, 100%, 50%)"
}

function MouseMoveOut(id) {
    document.getElementById(id).style.backgroundColor = "hsl(71, 73%, 54%)"
}